##Entrda de dados

n1 = int(input("Digite um número inteiro: "))
n2 = int(input("Digite um número inteiro: "))
n3 = int(input("Digite um número inteiro: "))
 

## Processamento

m = (n1 + n2 + n3) / 3

if (m >= 60) :
    print("Aprovado")
elif (m >= 40) :
    print("Recuperação")
else :
    print("Reprovado")

